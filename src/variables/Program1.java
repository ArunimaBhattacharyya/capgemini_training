package variables;

public class Program1 {
	public static void main(String[] args) {
		//variable -> block of memory used to store data
		// syntax -> data-type identifiers
		
		// in java default integer type data is always int and default decimal or point value is always double
		long a;
		a = 12700000000l; // in case of long you have to end the number with 'l'.
		System.out.println(a);
		
		double b = 10.5;
		System.out.println(b);
	}

}
