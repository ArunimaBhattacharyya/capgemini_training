package variables;

public class IntConversion {
	public static void main(String[] args) {
		int i = 70;
		
		byte b = (byte)i;
		System.out.println(b);
		
		short s = (short)i;
		System.out.println(s);
		
		long l = i;
		System.out.println(l);
		
		double d = i;
		System.out.println(d);
		
		float f = i;
		System.out.println(f);
		
		char c = (char)i;
		System.out.println(c);
	}

}
