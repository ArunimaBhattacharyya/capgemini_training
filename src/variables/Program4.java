package variables;

public class Program4 {
	
	public static void main(String[] args) {
		// type-casting
		
		byte b = 20; // capacity - 1 byte
		
		int b1 = b; // capacity - 4 bytes so we can store the byte value
		System.out.println(b1); 
		
		int a = 30;
		long a1 = a;
		System.out.println(a1);
	}

}
