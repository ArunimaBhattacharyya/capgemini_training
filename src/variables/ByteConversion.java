package variables;

public class ByteConversion {
	
	public static void main(String[] args) {
		byte b = 65;
		
		short s = b;
		System.out.println(s);
		
		int i = b;
		System.out.println(i);
		
		long l = b;
		System.out.println(l);
		
		char ch = (char)b;
		System.out.println(ch);
		
		double d = b;
		System.out.println(d);
		
		float f = b;
		System.out.println(f);
	}

}
