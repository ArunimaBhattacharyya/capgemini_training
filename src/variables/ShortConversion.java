package variables;

public class ShortConversion {
	public static void main(String[] args) {
		short s1 = 66;
		
		byte b1 = (byte)s1;
		System.out.println(b1);
		
		int i1 = s1;
		System.out.println(i1);
		
		long l1 = s1;
		System.out.println(l1);
		
		char ch1 = (char)s1;
		System.out.println(ch1);
		
		double d1 = s1;
		System.out.println(d1);
		
		float f1 = s1;
		System.out.println(f1);
	}

}
